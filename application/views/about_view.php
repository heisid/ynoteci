<h2>About Y-Note</h2>
<br>

<h3>What is this?</h3>
<p>Y-Note is stupidly simple note taking website: write a formatted note, read, edit, and delete. No unnecessary fuss you barely care. That's what I say. But you know the truth: it's simply because I'm a novice.</p>
<p>Y-Note is created using these stuffs:</p>
<ul>
    <li><a href="https://codeigniter.com" target="_blank">CodeIgniter</a> framework on top of <a href="http://www.php.net/" target="_blank">PHP</a> programming language.</li>
    <li><a href="https://ckeditor.com/" target="_blank">CKEditor</a> as its rich text editor, I'm too lazy to make my own. Even I didn't make any customization.</li>
</ul>
<p>That's it. Nothing fancy.</p>

<h3>Backstory</h3>
<p>Initially, Y-Note was made for devschool. But it turned out this thing is quite fun.</p>
<p>The name itself consists of "Y" and "note". "Y" is taken from my real name of how I'm called at home and devschool.
And it can be pronounced as "why not?", which I think is quite cool.</p>

